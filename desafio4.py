#operadores aritméticos

a = 10
b = 3

soma = a + b  # Soma
subtração = a - b  # Subtração
multiplicacao = a * b  # Multiplicação
divisão = a / b  # Divisão
divisao_inteira = a // b  # Divisão inteira
resto = a % b  # Resto da divisão
exponenciação = a ** b  # Exponenciação

print('A soma das variáveis A e B é: ', soma)
print('A subtraçao das variáveis A e B é: ', subtração)
print('A divisao das variáveis A e B é: ', divisão)
print('A divisão inteira das variáveis A e B é: ', divisao_inteira)
print('O resto da divisão das variáveis A e B é: ', resto)
print('A exponenciação das variáveis A e B é: ', exponenciação)