#média aritmética
nota1 = float(input('Qual foi a sua nota no primeiro bimestre? '))
nota2 = float(input('Qual foi a sua nota no segundo bimestre? '))
nota3 = float(input('Qual foi a sua nota no terceiro bimestre? '))
nota4= float(input('Qual foi a sua nota no quarto bimestre? '))
media = (nota1 + nota2 + nota3 + nota4) / 4
print(f'A sua nota no primeiro bimestre foi {nota1}, no segundo bimestre{nota2}, no terceiro bimestre {nota3} e no quarto bimestre{nota4}. Logo, a sua média final foi de {media}!')